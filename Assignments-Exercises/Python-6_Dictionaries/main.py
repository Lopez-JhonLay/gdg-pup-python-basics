# Step 1: Create a dictionary
book_info = {'title': 'The Great Adventure', 'pages': 350}
print("Original dictionary:", book_info)

# Step 2: Add a new key-value pair
book_info['author'] = 'Jane Doe'
print("Dictionary after adding an item:", book_info)

# Step 3: Update an existing key-value pair
book_info['pages'] = 375
print("Dictionary after updating an item:", book_info)

# Step 4: Remove a key-value pair
del book_info['pages']
print("Dictionary after removing an item:", book_info)